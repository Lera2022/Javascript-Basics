/**
 * 3.
 * Выполняет умножение чисел
 * @param {number} n1 Первое число, которое умножаем
 * @param {number} n2 Второе число, которое умножаем 
 * @returns {number} Результат умножения двух чисел
 */
function mult(n1, n2) {
  return n1 * n2;
}

/**
 * 4.
 * Выполняет деление чисел
 * @param {number} n1 Делимое число
 * @param {number} n2 Число-делитель
 * @returns {number} Результат деления первого числа на второе
 */
function div(n1, n2) {
  return n1 / n2;
}

