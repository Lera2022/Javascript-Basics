/**
 * 1.
 * Выполняет сложение двух чисел
 * @param {number} n1 Первое число, которое складываем
 * @param {number} n2 Второе число, которое складываем 
 * @returns {number} Результат сложения двух чисел
 */
function sum(n1, n2) {
  return n1 + n2;
}

/**
 * 2.
 * Вычисляет разность двух чисел
 * @param {number} n1 Первое число
 * @param {number} n2 Второе число
 * @returns {number} Результат вычитания меньшего числа из большего числа
 */
function sub(n1, n2) {
  return Math.max(n1, n2) - Math.min(n1, n2);
}

